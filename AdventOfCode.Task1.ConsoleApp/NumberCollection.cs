        public static List<int> CalcSlidingWindow(List<int> list, int n)
        {
            var result = new List<int>();

            if (list.Count < n) return null;

            int maxSum = 0;

            for (int i = 0; i < n; i++) maxSum += list[i];

            int p1 = 0;
            int p2 = n;
            int tempSum = maxSum;

            while (p2 < list.Count) 
            { 
                tempSum = tempSum - list[p1] + list[p2];

                result.Add(maxSum);
                maxSum = tempSum;

                p1++;
                p2++;
            }

            return result;
        }
